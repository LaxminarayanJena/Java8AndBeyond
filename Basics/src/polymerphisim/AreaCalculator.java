package polymerphisim;

public class AreaCalculator {
	
	public int getArea(int side)
	{
		return side*side;
	}

	public double getArea(double side)
	{
		return side*side;
	}
	
	public double getArea(int length,int width)
	{
		return length*width;
	}
	
	public double getArea(double length,int width)
	{
		return length*width;
	}
}
